-- @usage
-- > foo = Object:new{foo = 'foo'}
-- > foo.foo
-- foo
--
-- @function Object:new
Object.new = type_check({clone = 'function'}, '?table', '...')(
    function (proto, ...)
        return update(proto:clone(), ...)
    end
)

